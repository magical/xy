var littleEndian = binary.LittleEndian

func main() {
	f, err := os.Open(os.Args[1]) // a/2/1/4
	if err != nil {
		fmt.Println(err)
		return
	}
	defer f.Close()
	files, err := garc.Files(f)
	if err != nil {
		fmt.Println(err)
		return
	}
	br := bufio.NewReader(nil)
	for i, f := range files {
		br.Reset(f)
		if name := pokemonnames[i]; name != "" {
			fmt.Print(i, "-", name, "\n")
		} else {
			fmt.Print(i, "\n")
		}
		for {
			var b [4]byte
			_, err := br.Read(b[:])
			if err == io.EOF {
				break
			}
			if err != nil {
				fmt.Println(err)
				continue
			}
			move := int16(littleEndian.Uint16(b[0:2]))
			level := int16(littleEndian.Uint16(b[2:4]))
			if move == -1 {
				break
			}
			if name := movenames[move]; name != "" {
				fmt.Print("    ", level, "-", name, "\n")
			} else {
				fmt.Print("    ", level, "-", move, "\n")
			}
		}
		fmt.Print("\n")
	}
}

